package day17_WhileAndDo_WhileLoops;

public class WhileLoopIntro {
    public static void main(String[] args) {

        if (true){
            System.out.println("Wooden Spoon"); //executing one time
        }

        System.out.println("====================================================");

        while (true){//repeated if
            System.out.println("Wooden Spoon");// executing infinity time until condition is false
        }
    }
}
